// Open fan service report
function openFanReport(driveUrl, fanName) {
    window.open(driveUrl, '_blank');
}

// Load fans for the company
window.addEventListener('DOMContentLoaded', function() {
    const companyName = localStorage.getItem('companyName');
    const companyDisplay = document.getElementById('companyDisplay');
    const fanGrid = document.getElementById('fanGrid');
    
    if (!companyName) {
        // If no company name, redirect back to search
        window.location.href = 'index.html';
        return;
    }
    
    companyDisplay.textContent = companyName;
    
    // Normalize company name for lookup
    const normalizedCompanyName = companyName.toUpperCase().trim();
    
    // Get fans for this company
    const companyData = companyFans[normalizedCompanyName];
    
    if (!companyData || !companyData.fans || companyData.fans.length === 0) {
        fanGrid.innerHTML = '<p class="no-fans">No fans found for this company.</p>';
        return;
    }
    
    // Generate fan cards
    fanGrid.innerHTML = '';
    companyData.fans.forEach(fan => {
        const fanCard = document.createElement('div');
        fanCard.className = 'fan-card';
        fanCard.innerHTML = `
            <div class="fan-icon">
                <svg fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 3v2m6-2v2M9 19v2m6-2v2M5 9H3m2 6H3m18-6h-2m2 6h-2M7 19h10a2 2 0 002-2V7a2 2 0 00-2-2H7a2 2 0 00-2 2v10a2 2 0 002 2zM9 9h6v6H9V9z" />
                </svg>
            </div>
            <h3 class="fan-name">${fan.name}</h3>
            <button class="btn-fan" onclick="openFanReport('${fan.driveUrl}', '${fan.name}')">
                View Reports
            </button>
        `;
        fanGrid.appendChild(fanCard);
    });
});